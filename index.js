function shuffle(deck){
    let tempDeck = deck;
    for (let x = 0; x<365; x++){
        let random1 = Math.floor(Math.random()*deck.length);
        let random2 = Math.floor(Math.random()*deck.length);
        let temp;
        
        temp = tempDeck[random1];
        tempDeck[random1] = tempDeck[random2];
        tempDeck[random2] = temp;

    }
    return tempDeck;
    

}
